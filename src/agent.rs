    pub fn log_harness_event(&self, event: &str, content: &str) {
        if let Some(s) = &self.session {
            let entry = serde_json::json!({
                "ts": chrono::Utc::now().to_rfc3339(),
                "role": "system",
                "event": event,
                "content": content,
            });
            let _ = s.append_log(&entry.to_string());
        }
    }

