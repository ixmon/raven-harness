    /// Record a user request for metadata (last_user_request, goal seeding) and
    /// trace logging. Also pushes the (latest) user message into `conversation`
    /// as a normal user turn. This ensures the model receives fresh user inputs
    /// in proper alternating history (not only via the "Latest User Message"
    /// header in the injection block).
    pub fn record_user_request(&mut self, user_input: &str) {
        if let Some(s) = &mut self.session {
            let _ = s.set_last_user_request(user_input);
            if self.config.flags.goal_tracking {
                if self.config.flags.is_eval {
                    // Eval-specific goal seeding with heuristic to avoid chat fixation
                    let no_initial_goal = self.config.flags.no_initial_goal;
                    if !no_initial_goal
                        && (s.meta.current_goal.trim().is_empty()
                            || s.meta.current_goal.contains("not yet established"))
                    {
                        let lower = user_input.trim().to_lowercase();
                        if user_input.len() > 25
                            && !lower.starts_with("hello")
                            && !lower.starts_with("hi")
                            && !lower.starts_with("hey")
                            && !lower.contains("joke")
                            && !lower.contains("tool")
                        {
                            let g = tools::safe_truncate(user_input, GOAL_TRUNCATE);
                            let _ = s.update_goal(
                                &format!("Initial goal from user: {}", g),
                                None,
                                None,
                            );
                        }
                    }
                } else {
                    // For normal use with goal tracking enabled: seed from first
                    if s.meta.current_goal.contains("not yet established")
                        || s.meta.current_goal.trim().is_empty()
                    {
                        let g = tools::safe_truncate(user_input, GOAL_TRUNCATE);
                        let _ =
                            s.update_goal(&format!("Initial goal from user: {}", g), None, None);
                    }
                }
            }
        }

        // Log the request event to persistent full_log (for traces, evals, raven_log.jsonl).
        if let Some(s) = &self.session {
            let entry = serde_json::json!({
                "ts": chrono::Utc::now().to_rfc3339(),
                "role": "user",
                "content": user_input,
                "has_tool_calls": false,
            });
            let _ = s.append_log(&entry.to_string());
        }

        // Push into conversation so the model sees the latest user input as a
        // proper role:user turn in the messages array (fixes echo / missed follow-ups
        // in session mode).
        self.conversation.push(Message {
            role: "user".into(),
            content: Some(user_input.to_string()),
            tool_calls: None,
            tool_call_id: None,
        });

        // We directly logged the user entry. Advance so persist_turn (on tool-using
        // paths) won't duplicate-log this user entry from the conversation vec.
        if self.session.is_some() {
            self.logged_message_count = self.conversation.len();
        }

        // Make the define_done instruction explicitly visible in the full log
        // for debugging (e.g. "why didn't the agent call it?").
        if user_input.contains("define_done")
            || user_input.contains("Early in the task, call the `define_done`")
        {
            self.log_harness_event(
                "define_done_instruction",
                "Initial request contained explicit instruction to call define_done early (once, before heavy tool use / in first or second turn). This is critical for criteria-based judge nudging."
            );
        }
    }

    pub fn on_new_user_input(&mut self, user_input: &str) {
        // Delegates to record_user_request which now handles meta, logging,
        // and pushing the user message into conversation for all paths.
        self.record_user_request(user_input);
    }

    /// Build the exact messages array we will send to the model this turn.
    /// Always starts with a fresh system message containing the current
    /// repo cache + goal + pitfalls + recent summary from the Session.
    fn build_messages_for_model(&self) -> Vec<Message> {
        let mode = self.current_agent_mode();
        let base = system_message(
            &self.config.workspace,
            &self.config.flags,
            &mode,
            self.plan_tool_context(),
        );

        // Log the system prompt (including whether Core Loop was suppressed for evals)
        // into full_log on the very first build so we can see exactly what the model
        // was given for debugging (e.g. why define_done was or wasn't called).
        if self.conversation.is_empty() {
            if let Some(s) = &self.session {
                if let Some(content) = &base.content {
                    let entry = serde_json::json!({
                        "ts": chrono::Utc::now().to_rfc3339(),
                        "role": "system",
                        "content": content,
                        "note": "full system prompt used for this session (Core Loop omitted in RAVEN_EVAL mode)"
                    });
                    let _ = s.append_log(&entry.to_string());
                }
            }
        }

        let mut msgs = vec![];
        if let Some(s) = &self.session {
            let injection = s.get_injection_block(&self.config.flags, &self.current_agent_mode());
            let mut combined = if base.content.as_deref().unwrap_or("").is_empty() {
                injection
            } else {
                format!("{}\n\n{}", base.content.as_deref().unwrap_or(""), injection)
            };
            let plan_block = crate::plan_execution::format_plan_execution_injection(
                &self.plan_exec,
                &self.config.workspace,
            );
            if !plan_block.is_empty() {
                combined.push_str("\n\n");
                combined.push_str(&plan_block);
            }
            msgs.push(Message {
                role: "system".into(),
                content: Some(combined),
                tool_calls: None,
                tool_call_id: None,
            });
        } else {
            msgs.push(base);
        }

        // Append the actual conversation turns (already pruned)
        msgs.extend(self.conversation.iter().cloned());

        // Legacy synthetic user marker (eval-only) as a fallback if conversation is
        // somehow empty on first build for a session (e.g. after reset + record edge).
        // With user inputs now pushed to conversation, this is rarely reached.
        // Kept to protect against local servers that dislike pure-system prompts.
        if self.config.flags.is_eval && self.session.is_some() && self.conversation.is_empty() {
            msgs.push(Message {
                role: "user".into(),
                content: Some("Follow the Latest User Request in the SESSION CONTEXT above. Use tools now to explore and solve the task.".into()),
                tool_calls: None,
                tool_call_id: None,
            });
        }

        msgs
    }

    /// Handle session meta + file summary cache tools. Returns Some(ack) if handled.
    async fn handle_session_tool(&mut self, name: &str, args_json: &str) -> Option<String> {
        let args: serde_json::Value = serde_json::from_str(args_json).unwrap_or_else(|_| json!({}));

        if name == "complete_plan_step" {
            if self.current_agent_mode() != "work" || !self.plan_tool_context().plan_executing {
                return Some(
                    "❌ complete_plan_step is only available in work mode while an approved plan is executing."
                        .to_string(),
                );
            }
            let mut session_take = self.session.take();
            let outcome = crate::plan_execution::complete_plan_step(
                &mut self.plan_exec,
                &self.config.tool_backend,
                &self.config.workspace,
                session_take.as_mut(),
                &args,
            )
            .await;
            self.session = session_take;
            return Some(match outcome {
                crate::plan_execution::CompletePlanStepOutcome::Accepted { message } => {
                    format!("✓ {message}")
                }
                crate::plan_execution::CompletePlanStepOutcome::Failed { message } => {
                    format!("✗ {message}")
                }
                crate::plan_execution::CompletePlanStepOutcome::NeedsUserObservation {
                    prompt,
                    step_index,
                } => format!(
                    "⏸ Step {} requires user observation before continuing. Prompt shown to user: {}",
                    step_index + 1,
                    prompt
                ),
                crate::plan_execution::CompletePlanStepOutcome::Rejected { message } => {
                    format!("❌ {message}")
                }
            });
        }

        if name == "revise_plan_step" {
            let plan_executing = self.plan_tool_context().plan_executing;
            if self.current_agent_mode() != "plan"
                && !(self.current_agent_mode() == "work" && plan_executing)
            {
                return Some(
                    "❌ revise_plan_step is only available during plan mode or work mode with an active plan."
                        .to_string(),
                );
            }
            if let Some(s) = self.session.as_mut() {
                let exec = if plan_executing {
                    Some(&mut self.plan_exec)
                } else {
                    None
                };
                match crate::plan_execution::revise_plan_step(s, &args, exec) {
                    Ok(msg) => return Some(msg),
                    Err(e) => return Some(format!("❌ {e}")),
                }
            }
            return Some("❌ No session available for revise_plan_step.".to_string());
        }

        if name == "update_goal" {
            if let Some(s) = &mut self.session {
                let goal = args
                    .get("goal")
                    .and_then(|v| v.as_str())
                    .unwrap_or("")
                    .to_string();
                let tests = args.get("tests").and_then(|v| v.as_array()).map(|a| {
                    a.iter()
                        .filter_map(|x| x.as_str().map(|s| s.to_string()))
                        .collect::<Vec<_>>()
                });
                let pits = args.get("pitfalls").and_then(|v| v.as_array()).map(|a| {
                    a.iter()
                        .filter_map(|x| x.as_str().map(|s| s.to_string()))
                        .collect::<Vec<_>>()
                });
                let _ = s.update_goal(&goal, tests, pits);
                return Some(format!(
                    "Goal, tests and pitfalls updated in session meta. New goal: {}",
                    goal
                ));
            }
        }

        if name == "define_done" {
            if let Some(s) = &mut self.session {
                if s.meta.completion_criteria.is_none() {
                    let definition = args
                        .get("definition")
                        .and_then(|v| v.as_str())
                        .unwrap_or("")
                        .to_string();
                    if !definition.trim().is_empty() {
                        s.meta.completion_criteria = Some(definition.clone());
                        s.save_meta().ok();
                        self.log_harness_event(
                            "define_done_called",
                            &format!(
                                "Agent successfully called define_done. Criteria set to: {}",
                                definition
                            ),
                        );
                        return Some(format!("Completion criteria defined (set once). Judge will use this to decide when done and clear it on fulfillment: {}", definition));
                    }
                } else {
                    return Some("Completion criteria already set (one-time only). Judge clears it on completion.".to_string());
                }
            }
        }

        if name == "record_discovery" {
            if let Some(s) = &mut self.session {
                let text = args
                    .get("text")
                    .and_then(|v| v.as_str())
                    .unwrap_or("")
                    .to_string();
                let _ = s.record_discovery(&text);
                return Some(format!("Discovery recorded in session: {}", text));
            }
        }

        // Catch common hallucinated tool names early with a helpful message
        // (before falling through to normal tool execution).
        if matches!(name, "edit" | "str_replace" | "search_replace" | "modify_file") {
            return Some("❌ There is no 'edit' tool. The correct tool for search/replace edits is `patch` (parameters: path, search, replace, optional near_line; add wiki=true for the session wiki). Use `read` first to get exact text to search for.".to_string());
        }

        // Wiki flag: when wiki=true is set on read/write/patch/list, route to the
        // session's private wiki directory instead of the workspace.
        // Wiki writes are always allowed (no approval needed) — it's the agent's own scratchpad.
        let mut is_wiki = args.get("wiki").is_some_and(|v| {
            if let Some(b) = v.as_bool() {
                b
            } else if let Some(s) = v.as_str() {
                matches!(s.trim().to_lowercase().as_str(), "true" | "1" | "yes" | "on")
            } else if let Some(n) = v.as_i64() {
                n != 0
            } else {
                false
            }
        });
        // Auto-detect: if path starts with "wiki/" but wiki=true wasn't set,
        // the model forgot the flag. Route to wiki anyway (strip_wiki_prefix handles cleanup).
        if !is_wiki && matches!(name, "read" | "write" | "patch" | "list") {
            if let Some(p) = args.get("path").and_then(|v| v.as_str()) {
                let trimmed = p.trim().trim_start_matches("./");
                if trimmed.starts_with("wiki/") || trimmed.starts_with("wiki\\") || trimmed == "wiki" {
                    is_wiki = true;
                }
            }
        }
        if is_wiki && matches!(name, "read" | "write" | "patch" | "list") {
            if let Some(s) = &self.session {
                let path = args.get("path").and_then(|v| v.as_str()).unwrap_or("");
                match name {
                    "read" => {
                        let lines = args.get("lines").and_then(|v| v.as_str());
                        let full = args.get("full").is_some_and(|v| {
                            if let Some(b) = v.as_bool() { b } else if let Some(s) = v.as_str() {
                                matches!(s.trim().to_lowercase().as_str(), "true"|"1"|"yes")
                            } else if let Some(n) = v.as_i64() { n != 0 } else { false }
                        });
                        let limit = self.config.context_budget.read_line_limit;
                        return Some(s.read_wiki_file(path, lines, full, limit));
                    }
                    "write" => {
                        let content = args.get("content").and_then(|v| v.as_str()).unwrap_or("");
                        let _ = s.ensure_wiki_dir();
                        return Some(s.write_wiki_file(path, content));
                    }
                    "patch" => {
                        let search = args.get("search").and_then(|v| v.as_str()).unwrap_or("");
                        let replace = args.get("replace").and_then(|v| v.as_str()).unwrap_or("");
                        let near_line = args.get("near_line").and_then(|v| v.as_i64());
                        let _ = s.ensure_wiki_dir();
                        return Some(s.patch_wiki_file(path, search, replace, near_line));
                    }
                    "list" => {
                        let sub = if path.is_empty() { None } else { Some(path) };
                        let _ = s.ensure_wiki_dir();
                        return Some(s.list_wiki(sub));
                    }
                    _ => unreachable!(),
                }
            }
            return Some(format!("❌ {}(wiki=true): no active session", name));
        }

        if name == "read_summary" {
            let path = args.get("path").and_then(|v| v.as_str()).unwrap_or("");
            let rel = make_rel_path(path, &self.config.workspace);
            if let Some(s) = &self.session {
                if let Ok(Some((_, summary))) = s.get_file_summary(&rel) {
                    return Some(format!("📄 {} (fresh summary):\n{}", rel, summary));
                }
                // Cache miss or stale: deliver (capped) raw content + current mtime so the
                // caller can analyze and later call store_summary(path, mtime, summary).
                let raw = tools::read_file(
                    path,
                    None,
                    &self.config.workspace,
                    self.config.context_budget.read_line_limit,
                    false,
                );
                let mtime =
                    crate::session::current_file_mtime_for_agent(&self.config.workspace.join(&rel));
                return Some(format!(
                    "📄 {} (mtime={}, stale/missing - analyze below then call store_summary(path=\"{}\", mtime={}, summary=\"your concise summary\")):\n{}",
                    rel, mtime, path, mtime, raw
                ));
            }
            // Fallback (no session): just do a normal read
            let raw = tools::read_file(
                path,
                None,
                &self.config.workspace,
                self.config.context_budget.read_line_limit,
                false,
            );
            return Some(format!("📄 {}:\n{}", rel, raw));
        }

        if name == "store_summary" {
            if let Some(s) = &self.session {
                let p = args.get("path").and_then(|v| v.as_str()).unwrap_or("");
                let rel = make_rel_path(p, &self.config.workspace);
                let mtime = args.get("mtime").and_then(|v| v.as_i64()).unwrap_or(0);
                let summary = args.get("summary").and_then(|v| v.as_str()).unwrap_or("");
                let _ = s.store_file_summary(&rel, mtime, summary);
                return Some(format!("✅ summary stored for {} @ mtime={}", rel, mtime));
            }
            return Some("✅ store_summary (no session)".to_string());
        }

        None
    }

    fn maybe_invalidate_summary(&mut self, name: &str, args: &serde_json::Value) {
        if name == "write" || name == "patch" {
            if let Some(s) = &self.session {
                if let Some(p) = args.get("path").and_then(|v| v.as_str()) {
                    let rel = make_rel_path(p, &self.config.workspace);
                    let _ = s.invalidate_file_summary(&rel);
                }
            }
        }
    }

    async fn persist_turn(&mut self) {
        // Append all unlogged messages to full_log.jsonl (assistant + tool calls + results).
        if let Some(s) = &self.session {
            while self.logged_message_count < self.conversation.len() {
                let msg = &self.conversation[self.logged_message_count];
                let tool_names: Vec<&str> = msg
                    .tool_calls
                    .as_ref()
                    .map(|tcs| tcs.iter().map(|tc| tc.function.name.as_str()).collect())
                    .unwrap_or_default();

                // Include full tool call input details (name + arguments) when the
                // assistant message contains tool_calls. This makes tool *inputs*
                // available in full_log.jsonl for debugging/replay without logging
                // raw tool *outputs* (those are sanitized + truncated before being
                // put into the "tool" role messages that go to the model and the log).
                let tool_calls_detail = msg.tool_calls.as_ref().map(|tcs| {
                    tcs.iter().map(|tc| {
                        let args_val: serde_json::Value = serde_json::from_str(&tc.function.arguments)
                            .unwrap_or_else(|_| serde_json::json!(tc.function.arguments));
                        serde_json::json!({
                            "id": &tc.id,
                            "function": {
                                "name": &tc.function.name,
                                "arguments": args_val
                            }
                        })
                    }).collect::<Vec<_>>()
                });

                let entry = serde_json::json!({
                    "ts": chrono::Utc::now().to_rfc3339(),
                    "role": msg.role,
                    "content": msg.content,
                    "has_tool_calls": msg.tool_calls.is_some(),
                    "tool_call_id": msg.tool_call_id,
                    "tool_names": tool_names,
                    "tool_calls": tool_calls_detail,
                });
                let _ = s.append_log(&entry.to_string());
                self.logged_message_count += 1;
            }
            // Rolling recent-turns summary refresh. Don't do this too often: each
            // call consumes one LLM round (and for mock backends, depletes the
            // scripted response queue). The heavy prune_history at 48 messages
            // handles full compression.
            if self.conversation.len() >= 24
                && self.conversation.len().is_multiple_of(12)
                && !matches!(*self.client.lock().await, ChatBackend::Mock(_))
            {
                let last_few: Vec<_> = self.conversation.iter().rev().take(6).cloned().collect();
                if !last_few.is_empty() {
                    let sm = self.summarize_messages(&last_few).await;
                    let label = self.generate_session_label().await;
                    if let Some(s_mut) = &mut self.session {
                        let prev = &s_mut.meta.recent_turns_summary;
                        let merged = if prev.is_empty() {
                            sm
                        } else {
                            format!("{}\n{}", prev, sm)
                        };
                        let trimmed = if merged.len() > SUMMARY_CHAR_LIMIT {
                            tools::safe_truncate(&merged, SUMMARY_CHAR_LIMIT).to_string() + "..."
                        } else {
                            merged
                        };
                        let _ = s_mut.set_recent_turns_summary(&trimmed);

                        // Keep label fresh whenever we touch the summary
                        let _ = s_mut.set_session_label(&label);
                    }
                }
            }
        }
    }

    /// Force-update the (cheap) session label for the picker and, occasionally,
    /// the recent_turns_summary. Called at the end of each turn so that session
    /// state on disk is reasonably fresh if the process is killed/restarted.
    ///
    /// We deliberately do *not* summarize on every turn (or even every 5th).
    /// Summarization is expensive (an extra LLM call). The heavy lifting is in
    /// prune_history (at 48 msgs) and the lighter periodic refresh in persist_turn
    /// (every 12 msgs once >= 24). Here we only do a light refresh every 12 as well.
    pub async fn force_flush_session(&mut self) {
        if self.conversation.len() < 2 {
            return;
        }

        // Label is cheap (heuristic, no LLM call) — always refresh it so the
        // picker shows a good description even after restarts.
        let label = self.generate_session_label().await;

        let len = self.conversation.len();
        let is_mock = matches!(*self.client.lock().await, ChatBackend::Mock(_));

        let needs_full = {
            let s_ref = self.session.as_ref();
            s_ref.is_none_or(|s| s.meta.recent_turns_summary.is_empty())
                || (len >= 24 && len.is_multiple_of(12) && !is_mock)
        };

        let summary_for_meta = if needs_full {
            let recent: Vec<_> = self.conversation.iter().rev().take(6).cloned().collect();
            if recent.is_empty() {
                None
            } else {
                let sm = self.summarize_messages(&recent).await;
                let prev = self.session.as_ref()
                    .map(|s| s.meta.recent_turns_summary.clone())
                    .unwrap_or_default();
                let merged = if prev.is_empty() {
                    sm
                } else {
                    format!("{}\n{}", prev, sm)
                };
                Some(merged)
            }
        } else {
            None
        };

        if let Some(s) = &mut self.session {
            let _ = s.set_session_label(&label);

            if needs_full {
                if let Some(sm) = summary_for_meta {
                    let trimmed = if sm.len() > SUMMARY_CHAR_LIMIT {
                        tools::safe_truncate(&sm, SUMMARY_CHAR_LIMIT).to_string() + "..."
                    } else {
                        sm
                    };
                    let _ = s.set_recent_turns_summary(&trimmed);
                }
            }
        }
    }

